function infoGallery(index, lang) {
    var a = 
        `<div id="gallery-pop" class="gallery-pop">
        <div class="gallery-popback" onclick=closeGallery()></div>
        <div class="info-box" id="info-box">
            <h2>${data[index].name[lang]}</h2>
            <p>${data[index].description[lang]}</p>
            <div class="table">`
    var fol = data[index].folder;
    data[index].img.forEach(image => {
        a+=
        `<div class="img-title">
            <img src="${fol + image.dest}" alt="">
            <h2>${image.title[lang]}</h2>
        </div>`;        
    });
    a +=
            `</div>
        <div>
    </div>`;
    document.getElementById("gallery").innerHTML += a;
    setTimeout(() => {
        document.getElementById("info-box").style.opacity = "1";
    }, 1);
}

function closeGallery() {
    document.getElementById("gallery").removeChild(document.getElementById("gallery-pop"));
}